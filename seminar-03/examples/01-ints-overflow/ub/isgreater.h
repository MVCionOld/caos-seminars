#ifndef MIPT_ATP_CAOS_SEMINARS_ISGREATER_H
#define MIPT_ATP_CAOS_SEMINARS_ISGREATER_H

int signed_isgreater(int x) {
    return x + 1 > x;
}

int unsigned_isgreater(unsigned int x) {
    return x + 1 > x;
}

#endif //MIPT_ATP_CAOS_SEMINARS_ISGREATER_H
